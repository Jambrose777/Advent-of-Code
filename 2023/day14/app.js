function shiftEverythingToTheLeft(g) {
  return g.map(row => {
    let shiftIndex = 0;
    row.forEach((space, index) => {
      if (space === 'O') {
        row[index] = row[shiftIndex];
        row[shiftIndex] = 'O';
        shiftIndex++;
      } else if (space === '#') {
        shiftIndex = index + 1;
      }
    })
    return row;
  })
}

